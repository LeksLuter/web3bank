  Object.keys(translations[currentLanguage]).forEach(key => {
    const element = document.getElementById(key);
    if (element) {
      element.textContent = translations[currentLanguage][key];
    }
  });

  // –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ —Å–ø–∏—Å–∫–æ–≤
