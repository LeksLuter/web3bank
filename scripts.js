let currentUser = null;
let completedQuests = [];

// Загрузка сохраненных данных
function loadUserData() {
  const savedUser = localStorage.getItem("currentUser");
  const savedQuests = localStorage.getItem("completedQuests");
  
  if (savedUser) {
    currentUser = JSON.parse(savedUser);
    showUserProfile();
  }
  
  if (savedQuests) {
    completedQuests = JSON.parse(savedQuests);
  }
}

// Сохранение данных пользователя
function saveUserData() {
  if (currentUser) {
    localStorage.setItem("currentUser", JSON.stringify(currentUser));
  }
  localStorage.setItem("completedQuests", JSON.stringify(completedQuests));
}

// Показать форму логина
function showLoginForm() {
  document.getElementById("login-modal").classList.remove("hidden");
}

// Логин пользователя
function login() {
  const walletAddress = document.getElementById("wallet-address").value.trim();
  
  if (!walletAddress) {
    alert("Please enter your wallet address!");
    return;
  }
  
  currentUser = {
    wallet: walletAddress,
    loginTime: new Date().toISOString()
  };
  
  saveUserData();
  showUserProfile();
  document.getElementById("login-modal").classList.add("hidden");
  renderQuests();
}

// Показать профиль пользователя
function showUserProfile() {
  document.querySelector(".user-card").classList.remove("hidden");
  document.getElementById("user-wallet").textContent = currentUser.wallet;
  updateCompletedCount();
}

// Обновление счетчика выполненных заданий
function updateCompletedCount() {
  const count = completedQuests.length;
  document.getElementById("tasks-completed").textContent = count;
}

// Рендер квестов
function renderQuests() {
  const questsList = document.getElementById("quests-list");
  questsList.innerHTML = "";
  
  questData.forEach(quest => {
    const isCompleted = completedQuests.includes(quest.id);
    const title = translations[currentLanguage][quest.titleKey];
    
    const questCard = document.createElement("div");
    questCard.className = "quest-card";
    questCard.innerHTML = `
      <div class="quest-info">
        <h3>${title}</h3>
        <p class="quest-points">+${quest.points} WTB</p>
      </div>
      <button 
        onclick="completeQuest(${quest.id})" 
        class="quest-button ${isCompleted ? 'completed' : ''}"
        ${isCompleted ? 'disabled' : ''}
      >
        ${isCompleted 
          ? translations[currentLanguage]["quest-completed"] 
          : translations[currentLanguage]["quest-complete"]}
      </button>
    `;
    
    questsList.appendChild(questCard);
  });
}

// Выполнение квеста
function completeQuest(questId) {
  const quest = questData.find(q => q.id === questId);
  
  if (!quest || completedQuests.includes(questId)) {
    return;
  }
  
  // Если есть URL, открываем его в новой вкладке
  if (quest.url) {
    window.open(quest.url, "_blank");
  }
  
  // Для квеста с мемом показываем диалог
  if (questId === 4) {
    const memeUrl = prompt("Enter the link to your meme (Twitter/X post, Telegram, etc.):");
    if (!memeUrl) {
      return;
    }
    // Здесь можно добавить отправку URL мема на сервер
    console.log("User submitted meme:", memeUrl);
  }
  
  // Помечаем квест как выполненный
  completedQuests.push(questId);
  saveUserData();
  updateCompletedCount();
  renderQuests();
  
  // Показываем уведомление
  alert(`🎉 Congratulations! You earned ${quest.points} WTB tokens!`);
}

// Загрузка при инициализации страницы
